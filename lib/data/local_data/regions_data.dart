  static RegionModel? getRegionById(int id) {
    try {
      return regions.firstWhere((region) => region.id == id);
    } catch (e) {
      return null;
    }
  }

  static List<RegionModel> searchRegions(String query) {
    if (query.isEmpty) return regions;
    
    return regions.where((region) =>
        region.nameUz.toLowerCase().contains(query.toLowerCase()) ||
        region.nameRu.toLowerCase().contains(query.toLowerCase()) ||
        region.nameEn.toLowerCase().contains(query.toLowerCase()) ||
        region.capitalUz.toLowerCase().contains(query.toLowerCase()) ||
        region.capitalRu.toLowerCase().contains(query.toLowerCase()) ||
        region.capitalEn.toLowerCase().contains(query.toLowerCase())
    ).toList();
  }

  static List<RegionModel> getRegionsByPopulationRange(int minPopulation, int maxPopulation) {
    return regions.where((region) =>
        region.population >= minPopulation && region.population <= maxPopulation
    ).toList();
  }

  static List<RegionModel> getRegionsByAreaRange(double minArea, double maxArea) {
    return regions.where((region) =>
        region.area >= minArea && region.area <= maxArea
    ).toList();
  }
